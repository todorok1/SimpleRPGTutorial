using UnityEngine;

namespace SimpleRpg
{
    /// <summary>
    /// 装備品の補正値を定義するクラスです。
    /// </summary>
    public class CorrectedValue
    {
        /// <summary>
        /// 攻撃力の補正値です。
        /// </summary>
        public int strength;

        /// <summary>
        /// 防御力の補正値です。
        /// </summary>
        public int guard;

        /// <summary>
        /// 素早さの補正値です。
        /// </summary>
        public int speed;
    }
}